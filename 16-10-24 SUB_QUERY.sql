use scott;
-- 1.⁠ ⁠WAQTD ENAME AND SALARY OF ALL THE EMPLOYEES WHO ARE EARNING MORE THAN MILLER BUT LESS THAN ALLEN
SELECT ENAME,SAL
FROM EMP
WHERE SAL > (SELECT SAL FROM EMP WHERE ENAME = 'MILLER') AND SAL < (SELECT SAL FROM EMP WHERE ENAME = 'ALLEN');

-- 2.⁠ ⁠WAQTD ALL THE DETAILS OF THE EMPLOYEES WORKING IN DEPT 20 AND WORKING IN THE SAME DESIGNATION AS SMITH
SELECT *
FROM EMP
WHERE DEPTNO = 20 AND JOB = (SELECT JOB FROM EMP WHERE ENAME = 'SMITH');

 -- 3. WAQTD ALL THE DETAILS OF THE EMPLOYEES WORKING AS MANAGER IN THE SAME DEPT AS TURNER
 SELECT *
 FROM EMP
 WHERE JOB = (SELECT JOB FROM EMP WHERE ENAME = 'TURNER') AND JOB = 'MANAGER';

-- 4.⁠ ⁠WAQTD NAME AND HIREDATE OF THE EMPLOYEES HIRED AFTER 1980 AND BEFORE KING
SELECT ENAME, HIREDATE
FROM EMP
WHERE HIREDATE > '1980-12-31' AND HIREDATE < (SELECT HIREDATE FROM EMP WHERE ENAME = 'KING');

-- 5.⁠ ⁠WAQTD NAME AND SAL ALONG WITH ANNUAL SAL FOR ALL EMPLOYEES WHOS SAL IS LESS THAN BLAKE AND MORE THAN 3500
SELECT ENAME,SAL, SAL*12
FROM EMP
WHERE SAL < (SELECT SAL FROM EMP WHERE ENAME = 'BLAKE') AND SAL > 3500;

-- 6.⁠ ⁠WAQTD ALL THE DETAILS OF EMPLOYEES WHO EARN MORE THAN SCOTT BUT LESS THAN KING
SELECT *
FROM EMP
WHERE SAL > (SELECT SAL FROM EMP WHERE ENAME = 'SCOTT') AND SAL < (SELECT SAL FROM EMP WHERE ENAME = 'KING');

-- 7.⁠ ⁠WAQTD NAME OF THE EMPLOYEES WHOS NAME STARTS WITH 'A' AND WORKS IN THE SAME DEPT AS BLAKE.
SELECT ENAME
FROM EMP
WHERE ENAME LIKE 'A%' AND DEPTNO = (SELECT DEPTNO FROM EMP WHERE ENAME = 'BLAKE');

-- 8.⁠ ⁠WAQTD NAME AND COMM IF EMPLOYEES EARN COMISSION AND WORK IN THE SAME DESIGNATION AS SMITH
SELECT ENAME, COMM
FROM EMP
WHERE COMM IS NOT NULL AND JOB = (SELECT JOB FROM EMP WHERE ENAME = 'SMITH');

-- 9.⁠ ⁠WAQTD DETAILS OF ALL THE EMPLOYEES WORKING AS CLERK IN THE SAME DEPT AS TURNER
SELECT *
FROM EMP
WHERE JOB = 'CLERK' AND DEPTNO = (SELECT DEPTNO FROM EMP WHERE ENAME = 'TURNER');


-- 10.⁠ ⁠WAQTD ENAME, SAL AND DESIGNATION OF THE EMPLOYEES WHOS ANNUAL SALARY IS MORE THAN SMITH AND LESS THAN KING
